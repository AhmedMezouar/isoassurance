export const getters = {
    getT: (state) => (value) => {
        //return state.country.find((c) => c.name.en === value);
        let c = state.t.find((a)=>a.word.toLowerCase()===value.toLowerCase())
        if (c) {
            switch (state.lang) {
                case "en":
                    return c.en;
                case "fr":
                    return c.fr;
                case "ar":
                    return c.ar;
                case "tu":
                    return c.tu;
                default:
                    return value;
            }
        }else{
            return value
        }
    },
    getL(state) {
        return state.lang;
      },
  };
